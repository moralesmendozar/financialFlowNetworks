def fill(AR,AC,qt):
    if qt+AR[0]>AC[0]:
        qt2=qt+AR[0]-AC[0]
        AR[0]=AC[0]
        if qt2+AR[1]>AC[1]:
            qt3=qt2+AR[1]-AC[1]
            AR[1]=AC[1]
            AR[2]+=qt3
        else: AR[1]+=qt2
    else: AR[0]+=qt
    return AR

