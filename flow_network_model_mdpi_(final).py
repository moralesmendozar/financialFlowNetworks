# ---------------------------------------------------------------------------
# Waterfall function (matches your MDPI implementation exactly)
# ---------------------------------------------------------------------------

def fill(AR, AC, qt):
    """
    Loss waterfall used in the Flow Network model:
      1) Equity absorbs first, up to its capacity
      2) Interbank absorbs next, up to its capacity
      3) Any residual goes to external debt

    AR: residual vector [Equity_loss, Interbank_residual, ExtDebt_loss]
    AC: capacity vector  [Equity_cap,   Interbank_cap,   ExtDebt_cap]
    qt: incoming shock
    """
    if qt + AR[0] > AC[0]:
        qt2 = qt + AR[0] - AC[0]
        AR[0] = AC[0]
        if qt2 + AR[1] > AC[1]:
            qt3 = qt2 + AR[1] - AC[1]
            AR[1] = AC[1]
            AR[2] += qt3
        else:
            AR[1] += qt2
    else:
        AR[0] += qt
    return AR


# ---------------------------------------------------------------------------
# Sensitivity runner for a single quarter
# ---------------------------------------------------------------------------

def run_flow_network_quarter_sensitivity(
    MC,
    shock,
    max_share_pct=0.25,
    quarter_name="Q4 2008",
    show_progress=True
):
    """
    Run the Flow Network Model for a single quarter with:
      - MC: 4x3 balance-sheet matrix (millions USD)
      - shock: scalar shock (millions USD)
      - max_share_pct: single-counterparty cap (e.g., 0.25 for 25%)
      - show_progress: if True, show tqdm progress bar

    Returns a dict with:
      Mean_SR, P_at_least_1, P_at_least_2, Expected_failures, VaR95_SR, CVaR95_SR, ...
    """

    max_share = int(round(max_share_pct * 100))   # e.g., 0.25 -> 25

    PMT = list(permutations([0, 1, 2, 3]))

    E = []
    H = []
    PH = [0, 0, 0, 0, 0]  # counts of 0,1,2,3,4 failures
    count = 0

    # --------- Precompute total number of scenarios for the progress bar ---------
    base_per_l_n = max_share * max_share
    pair_count = sum((max_share + 1 - i) for i in range(1, max_share + 1))
    total_scenarios = len(PMT) * pair_count * base_per_l_n

    pbar = tqdm(
        total=total_scenarios,
        disable=not show_progress,
        desc=f"{quarter_name} | shock={shock:,.0f}M, cap={int(max_share_pct*100)}%"
    )

    # -----------------------------------------------------------------------
    # MAIN ENUMERATION (identical logic to your simulation, with progress)
    # -----------------------------------------------------------------------
    for c in range(len(PMT)):
        T1C = MC[PMT[c][0]]
        T2C = MC[PMT[c][1]]
        T3C = MC[PMT[c][2]]
        T4C = MC[PMT[c][3]]

        for i in range(1, max_share + 1):
            for j in range(1, max_share + 1 - i):

                # We'll update the progress bar once per (i,j) pair
                # after looping over all (l,n)
                scenarios_this_ij = 0

                for l in range(1, max_share + 1):
                    for n in range(1, max_share + 1):
                        # Residual vectors: [E_loss, IB_residual, H_loss]
                        T1R = [0, 0, 0]
                        T2R = [0, 0, 0]
                        T3R = [0, 0, 0]
                        T4R = [0, 0, 0]

                        # Initial shock into T1
                        T1R = fill(T1R, T1C, shock)

                        # First interaction: distribute T1R[1] (interbank residual) to T2, T3, T4
                        k = 100 - i - j
                        T2R = fill(T2R, T2C, T1R[1] * (i / 100.0))
                        T3R = fill(T3R, T3C, T1R[1] * (j / 100.0))
                        T4R = fill(T4R, T4C, T1R[1] * (k / 100.0))
                        T1R[1] = 0

                        # Second interaction: distribute T2R[1] to T3 and T4
                        m = 100 - l
                        T3R = fill(T3R, T3C, T2R[1] * (l / 100.0))
                        T4R = fill(T4R, T4C, T2R[1] * (m / 100.0))
                        T2R[1] = 0

                        # Third interaction: distribute T3R[1] to T4 / kept at T3
                        T4R = fill(T4R, T4C, T3R[1] * (n / 100.0))
                        T3R[1] -= T3R[1] * (n / 100.0)

                        count += 1
                        scenarios_this_ij += 1

                        # Aggregate equity and external-debt losses
                        E.append(T1R[0] + T2R[0] + T3R[0] + T4R[0])
                        H.append(T1R[2] + T2R[2] + T3R[2] + T4R[2])

                        # Failures: number of banks with non-zero interbank residual
                        alive_flags = [T1R[1], T2R[1], T3R[1], T4R[1]]
                        n_fail = 4 - alive_flags.count(0)
                        PH[n_fail] += 1

                # Update progress bar once for all (l,n) for this (i,j)
                pbar.update(base_per_l_n)

    pbar.close()

    # -----------------------------------------------------------------------
    # Aggregate statistics
    # -----------------------------------------------------------------------
    E_arr = np.array(E)
    H_arr = np.array(H)
    SR_arr = E_arr + H_arr

    p_0 = PH[0] / count
    p_1 = PH[1] / count
    p_2 = PH[2] / count
    p_3 = PH[3] / count
    p_4 = PH[4] / count
    p_ge_1 = 1.0 - p_0
    p_ge_2 = 1.0 - (p_0 + p_1)
    expected_failures = sum(i * PH[i] / count for i in range(5))

    q95 = np.quantile(SR_arr, 0.95)
    tail = SR_arr[SR_arr >= q95]
    cvar95 = float(tail.mean()) if tail.size > 0 else float(q95)

    results = {
        "Quarter": quarter_name,
        "Shock": shock,
        "Shock_mult": shock / 442000.0,
        "Cap_pct": max_share_pct,
        "Mean_E": float(E_arr.mean()),
        "Mean_H": float(H_arr.mean()),
        "Mean_SR": float(SR_arr.mean()),
        "Std_SR": float(SR_arr.std()),
        "P_0_banks": p_0,
        "P_1_bank": p_1,
        "P_2_banks": p_2,
        "P_3_banks": p_3,
        "P_4_banks": p_4,
        "P_at_least_1": p_ge_1,
        "P_at_least_2": p_ge_2,
        "Expected_failures": expected_failures,
        "VaR95_SR": float(q95),
        "CVaR95_SR": float(cvar95),
        "N_scenarios": count,
    }

    # Free arrays quickly
    del E_arr, H_arr, SR_arr

    return results


