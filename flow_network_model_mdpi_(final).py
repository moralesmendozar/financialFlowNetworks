# @title Utility function required by sensitivity analysis

def fill(R, C, Q):
    """
    Apply the three-stage loss waterfall from your paper:
      R : residual vector [Equity_loss, Interbank_residual, ExtDebt_loss]
      C : capacity vector  [Equity_cap, Interbank_cap, ExtDebt_cap]
      Q : incoming shock
    """
    e_cap, d_cap, h_cap = C  # capacities for equity, interbank, external debt
    e_res, d_res, h_res = R  # residual losses

    # Step 1 — Equity absorbs first
    a = min(Q, e_cap - e_res)
    e_res += a
    Q -= a

    # Step 2 — Interbank absorbs next (produces contagion loss)
    b = min(Q, d_cap - d_res)
    d_res += b
    Q -= b

    # Step 3 — External debt absorbs the rest
    c = min(Q, h_cap - h_res)
    h_res += c
    Q -= c

    # Update the residual vector
    return [e_res, d_res, h_res]

